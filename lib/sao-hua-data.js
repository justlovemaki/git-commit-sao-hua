function getSaoHua(type, style) {
    const messages = saoHuaData[type]?.[style];
    if (!messages || messages.length === 0) {
        return '骚话获取失败，请重试~';
    }
    return messages[Math.floor(Math.random() * messages.length)];
}

function getRandomSaoHua() {
    const types = Object.keys(saoHuaData);
    const randomType = types[Math.floor(Math.random() * types.length)];
    const styleKeys = Object.keys(saoHuaData[randomType]);
    const randomStyle = styleKeys[Math.floor(Math.random() * styleKeys.length)];
    return {
        type: randomType,
        message: saoHuaData[randomType][randomStyle][Math.floor(Math.random() * saoHuaData[randomType][randomStyle].length)],
        style: randomStyle
    };
}

function generateCommitMessage(type, style, description = '') {
    const message = getSaoHua(type, style);
    if (description) {
        return `${type}: ${description}\n\n${message}`;
    }
    return `${type}: 你的项目\n\n${message}`;
}

