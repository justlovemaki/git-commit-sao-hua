function getSaoHua(type, style, language = defaultLanguage) {
    const langData = saoHuaData[language] || saoHuaData[defaultLanguage];
    const messages = langData[type]?.[style];
    if (!messages || messages.length === 0) {
        return language === 'en' ? 'Failed to get message, please try again~' : '骚话获取失败，请重试~';
    }
    return messages[Math.floor(Math.random() * messages.length)];
}

function getRandomSaoHua(language = defaultLanguage) {
    const langData = saoHuaData[language] || saoHuaData[defaultLanguage];
    const types = Object.keys(langData);
    const randomType = types[Math.floor(Math.random() * types.length)];
    const styleKeys = Object.keys(langData[randomType]);
    const randomStyle = styleKeys[Math.floor(Math.random() * styleKeys.length)];
    return {
        type: randomType,
        message: langData[randomType][randomStyle][Math.floor(Math.random() * langData[randomType][randomStyle].length)],
        style: randomStyle,
        language: language
    };
}

function generateCommitMessage(type, style, description = '', language = defaultLanguage) {
    const message = getSaoHua(type, style, language);
    const descText = language === 'en' ? 'your project' : '你的项目';
    if (description) {
        return `${type}: ${description}\n\n${message}`;
    }
    return `${type}: ${descText}\n\n${message}`;
}

